        private void tampil() {
            
        cetak.hasilBarang.setText(cbBarang.getSelectedItem().toString());
        cetak.hasilTanggal.setText(tfTanggal.getText());
        cetak.hasilHarga.setText(tfHarga.getText());
        cetak.hasilJumlah.setText(tfJumlah.getText());
        cetak.hasilTotal.setText(tfTotal.getText());
        
        }
