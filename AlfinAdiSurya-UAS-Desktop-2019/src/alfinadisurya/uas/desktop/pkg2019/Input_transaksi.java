    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        if (cbBarang.getSelectedIndex() == 1){
            JOptionPane.showMessageDialog(null, "Silahkan Pilih Barang");
        }
        else if(tfTanggal.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Silahkan Masukkan Tanggal");
        }
        else if(tfHarga.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Silahkan Masukkan Harga");
        }
        else if(tfJumlah.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Silahkan Masukkan Jumlah");
        }
        else if(tfTotal.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Silahkan Masukkan Total");
        }
        else{
            tampil();
        }
        cetak.setVisible(true);
        
    }//GEN-LAST:event_btnSubmitActionPerformed

